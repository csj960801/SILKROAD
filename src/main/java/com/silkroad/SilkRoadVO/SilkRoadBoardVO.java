package com.silkroad.SilkRoadVO;

import java.io.Serializable;

public class SilkRoadBoardVO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1938513515011147028L;
	
	private String clientEmail;
	private String clientInquiry;

	public SilkRoadBoardVO() {
	}

	public SilkRoadBoardVO(String clientEmail, String clientInquiry) {
		this.setClientEmail(clientEmail);
		this.setClientInquiry(clientInquiry);
	}

	public String getClientEmail() {
		return clientEmail;
	}

	public void setClientEmail(String clientEmail) {
		this.clientEmail = clientEmail;
	}

	public String getClientInquiry() {
		return clientInquiry;
	}

	public void setClientInquiry(String clientInquiry) {
		this.clientInquiry = clientInquiry;
	}

	@Override
	public String toString() {
		return "SilkRoadVO[clientEmail: " + clientEmail + " , clientInquiry: " + clientInquiry + "]";
	}

}